x <- read.csv("../csv/FILTERED_RF_BT.csv",header=T,sep=",")
x <- t(x)
jml <- as.numeric(x[2,1:52])
jsat <- as.numeric(x[2,53:103])
weka <- as.numeric(x[2,104:ncol(x)])
png("../img/FILTERED_RF_BT.png",500,400)
boxplot(jml,jsat,weka,names=c("Java-ML","JSAT","WEKA"),xlab="Framework",ylab="Error Rate",ylim=c(0,30))
graphics.off()

